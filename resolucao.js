function somar(a, b) { // Função que recebe dois parâmetros e retorna a soma deles
  return a + b;
}

let x = 5; // let é uma variável que pode ser alterada
const y = 5; // const é uma variável que não pode ser alterada
x = x + 10; // x = 15

console.log(somar(x, y)); // 15 + 5 = 20